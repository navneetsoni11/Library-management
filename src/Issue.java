    private void issueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueButtonActionPerformed
        // TODO add your handling code here:
        try{
            conn=DataMain.connectSql();
            pst=conn.prepareStatement("select isissued from libbooks where name=?");
            pst.setString(1,bookname.getText());
            rs=pst.executeQuery();
            if(rs.next())
            {
                if(rs.getInt(1)==0)
                {
                     pst=conn.prepareStatement("update libbooks set issuetoid=?, isissued=? where id=?");
                    pst.setInt(1,Integer.parseInt(userid.getText()));
                    pst.setInt(2,1);
                    pst.setInt(3,Integer.parseInt(bookid.getText()));
                    rs=pst.executeQuery();
                    if(rs.next())
                    {
                        JOptionPane.showMessageDialog(this, "Book Issued.");
                        bookid.setText("");
                        bookname.setText("");
                        userid.setText("");
                        userid.setEditable(false);
                        bookid.setEditable(true);
                        bookname.setEditable(true);
                
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Book is issued already");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Issue unsuccessful");
                }
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e);
        }
       
    }//GEN-LAST:event_issueButtonActionPerformed

