    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            conn=DataMain.connectSql();          
            pst=conn.prepareStatement("select id,name from libbooks where id=? or name=?");
            pst.setInt(1,Integer.parseInt(bookid.getText()));
            pst.setString(2, bookname.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"Book found, Enter your UserId to Continue..");
                bookid.setText(Integer.toString(rs.getInt(1)));
                bookname.setText(rs.getString(2));
                bookid.setEditable(false);
                bookname.setEditable(false);
                userid.setEditable(true);
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Book not found");

            }
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

