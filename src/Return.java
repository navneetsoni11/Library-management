    private void returnButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnButtonActionPerformed
        // TODO add your handling code here:
         try{
            conn=DataMain.connectSql();
            pst=conn.prepareStatement("update libbooks set issuetoid=?, isissued=? where id=?");
            pst.setInt(1,0);
            pst.setInt(2,0);
            pst.setInt(3,Integer.parseInt(bookid.getText()));
            rs=pst.executeQuery();
            if(rs.next())
            {
                
                JOptionPane.showMessageDialog(this, "Book Returned");
                bookid.setText("");
                userid.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this,"Problem returning the book");
            }
            
        }
        catch(Exception e){
            
           JOptionPane.showMessageDialog(this,e);
        }
    }//GEN-LAST:event_returnButtonActionPerformed

