    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try{
            conn=DataMain.connectSql();
            pst=conn.prepareStatement("select issuetoid from libbooks where id=?");
            pst.setInt(1,Integer.parseInt(bookid.getText()));
            rs=pst.executeQuery();
            if(rs.next())
            {
                if(rs.getInt(1)==0)
                {
                    JOptionPane.showMessageDialog(this,"Book not issued");
                    
                }   
                else{
                userid.setText(Integer.toString(rs.getInt(1)));
                JOptionPane.showMessageDialog(this,"Found");
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"Cant find the userid");
                
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_searchActionPerformed

